package com.berryspace.conjure;

public class Artist {
    private String name;
    private String imageUrl;

    public void setName(String name){
        this.name = name;
    }

    public void setImageUrl(String imageUrl){
        this.imageUrl = imageUrl;
    }

    public String getName() {
        return name;
    }

    public String getImageUrl(){
        return imageUrl;
    }
}
